package com.weathernews.Weather;

public class ThemeInfo {
	private String szThemeCode;
	private String szCityID;
	private String szCityName;
	private long nIndex;
	private int nThemeID;
	
	public ThemeInfo(){
	}
	
	public ThemeInfo(String ThemeCode, String CityID, String CityName, long idx, int themeid){
		this.szThemeCode = ThemeCode;
		this.szCityID = CityID;
		this.szCityName = CityName;
		this.nIndex = idx;
		this.nThemeID = themeid;
	}
	
	public String getThemeCode(){
		return szThemeCode;
	}
	
	public String getCityID(){
		return szCityID;
	}
	
	public String getCityName(){
		return szCityName;
	}
	
	public long getIndex(){
		return nIndex;
	}
	
	public int getThemeID(){
		return nThemeID;
	}
}